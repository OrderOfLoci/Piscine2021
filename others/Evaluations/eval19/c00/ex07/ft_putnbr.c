#include<unistd.h>

void	ft_write(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 10)
	{
		ft_write(nb + '0');
	}
	else
	{
		ft_write('0' + nb / 10);
		ft_write('0' + nb % 10);
	}
}
