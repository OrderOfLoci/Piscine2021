#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	ft_print_program_name(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		ft_putchar(str[index]);
		index++;
	}
}

int	main(int argc, char **argv)
{
	while (argc != 0)
	{
		ft_print_program_name(*argv);
		argc--;
	}
	ft_putchar('\n');
	return (0);
}
