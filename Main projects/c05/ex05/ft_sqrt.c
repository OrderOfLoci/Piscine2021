int	ft_sqrt(int nb)
{
	int	n;

	n = 0;
	if (nb <= 0)
		return (0);
	while (n < 46340)
	{
		if (((n+1) * (n+1) > nb) && (n * n <= nb))
			return n;
		n++;
	}
	return (n);
}
