#include <unistd.h>

int main()
{
	char	c;
	char	d;

	c = 'a';
	d = 'B';

	while (c <= 'y' && d <= 'Z')
	{
		write(1, &c, 1);
		write(1, &d, 1);
		c = c + 2;
		d = d + 2;
	}
	write(1, "\n", 1);
}
