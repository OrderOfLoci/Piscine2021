#include <unistd.h>

int main()
{
	char	c;
	char	d;

	c = 'z';
	d = 'Y';

	while (c >= 'b' && d >= 'A')
	{
		write(1, &c, 1);
		write(1, &d, 1);
		c = c - 2;
		d = d - 2;
	}
	write(1, "\n", 1);
}
