#include <unistd.h>

void	printnum(int n)
{
	if (n > 9)
		printnum(n / 10);
	write(1,&"0123456789"[n % 10], 1);
}

int main(int argc, char **argv)
{
	(void)argv;
	printnum(argc-1);
	write(1, "\n", 1);
}
