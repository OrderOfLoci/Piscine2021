int	ft_strncmp(char *s1, char*s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	aux;

	i = 0;
	aux = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && !aux  && i < n)
	{
			aux = s1[i] - s2[i];
		i++;
	}
	if (i < n && !aux  && (s1[i] == '\0' || s2[i] == '\0'))
			aux = s1[i] - s2[i];

	return (aux);
}
